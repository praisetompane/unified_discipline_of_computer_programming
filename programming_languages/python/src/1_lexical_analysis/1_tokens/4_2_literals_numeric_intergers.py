a_number = 1
print(a_number)

print("negative integers:")
# '-' is a unary operator, which negates '1'
a_negative_number = -1
print(a_negative_number)

print("underscored integers:")
an_underscored_number = 100_000_000
print(an_underscored_number)
print(an_underscored_number + 1)

print("binary integers:")
binary_integer_3 = 0b0000_0011
print(binary_integer_3)

binary_integer_1 = 0b00000001
print(binary_integer_1)

print("octal integers:")
octal_integer_79 = 0o117
print(octal_integer_79)

octal_integer_15 = 0o01_7
print(octal_integer_15)

print("hexadecimal integers:")
hexadecimal_integer = 0xdeadbeef
print(hexadecimal_integer)

hexadecimal_integer_144 = 0x9_0
print(hexadecimal_integer_144)
